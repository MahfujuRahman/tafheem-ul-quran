<script>
    document.addEventListener("DOMContentLoaded", () => {
        const headings = document.querySelectorAll(".heading_buttons");

        headings.forEach(heading => {
            const buttons = heading.querySelectorAll("div");
            const activeBg = document.createElement("div");
            activeBg.classList.add("active-bg");
            heading.appendChild(activeBg);

            function moveActiveBg(target) {
                let targetRect = target.getBoundingClientRect();
                let parentRect = heading.getBoundingClientRect();
                let newLeft = targetRect.left - parentRect.left;

                activeBg.style.transform = `translate(${newLeft}px, -50%)`;
                activeBg.style.width = `${target.offsetWidth}px`;
            }

            buttons.forEach(button => {
                button.addEventListener("click", function() {
                    buttons.forEach(btn => btn.classList.remove("active"));
                    this.classList.add("active");
                    moveActiveBg(this);
                });
            });

            // Initialize with first button active
            if (buttons.length > 0) {
                buttons[0].classList.add("active");
                moveActiveBg(buttons[0]);
            }
        });
    });
</script>
